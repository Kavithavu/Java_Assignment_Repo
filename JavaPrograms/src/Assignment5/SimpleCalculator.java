package Assignment5;

import java.util.Scanner;

public class SimpleCalculator {

	public static int Add(int a,int b)
	{
		
		return (a+b);
		
	}
	public static int Substract(int a,int b)
	{
		
		return (a-b);
		
	}
	public static int Multiply(int a,int b)
	{
		
		return (a*b);
		
	}
	public static int Divide(int a,int b)
	{
		
		return (a/b);
		
	}
}

